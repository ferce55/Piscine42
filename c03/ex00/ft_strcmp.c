int	ft_strcmp(char *s1, char *s2)
{
	int	x;

	while (*s1 == *s2)
	{
		if (*s1 == '\0' && *s2 == '\0')
		{
			x = 0;
			return (x);
		}
		s1++;
		s2++;
	}
	x = *s1 - *s2;
	return (x);
}
